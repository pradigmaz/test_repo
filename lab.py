import math

def calculate_factory():
    print("--- Расчет пельменного цеха (Полный) ---")
    
    try:
        # Данные для этапа 1
        Q_sut = float(input("Введите суточную выработку (т): "))
        t_smena = float(input("Введите длительность смены (ч): "))
        p_pa = float(input("Производительность пельменного автомата (т/ч): "))
        
        # Данные для этапа 2 (добавил второй разработчик)
        print("\n--- Данные для теста и фарша ---")
        a_testo = float(input("Доля теста в продукции (%): "))
        p_tm = float(input("Производительность тестомеса (т/ч): "))
        
        a_farsh = float(input("Доля фарша в продукции (%): "))
        p_kutter = float(input("Производительность куттера (т/ч): "))
        
    except ValueError:
        print("Ошибка ввода! Введите числа.")
        return

    # --- ЭТАП 1: Пельменные автоматы ---
    P_tlp = Q_sut / (2 * t_smena)
    n_pa = math.ceil(P_tlp / p_pa)

    # --- ЭТАП 2: Тестомесы и Куттеры (код второго разработчика) ---
    
    # Линия подготовки теста
    P_tlt = P_tlp * (a_testo / 100)
    n_tm = math.ceil(P_tlt / p_tm)
    
    # Линия подготовки фарша
    P_tlf = P_tlp * (a_farsh / 100)
    n_k = math.ceil(P_tlf / p_kutter)

    # --- Вывод итогов ---
    print(f"\n{'='*30}")
    print(f"ИТОГОВЫЙ РАСЧЕТ ОБОРУДОВАНИЯ")
    print(f"{'='*30}")
    print(f"1. Пельменные автоматы:  {n_pa} шт. (нагрузка линии: {P_tlp:.3f} т/ч)")
    print(f"2. Тестомесильные машины: {n_tm} шт. (нагрузка линии: {P_tlt:.3f} т/ч)")
    print(f"3. Куттеры:               {n_k} шт. (нагрузка линии: {P_tlf:.3f} т/ч)")
    print(f"{'='*30}")

if __name__ == "__main__":
    calculate_factory()
